    public boolean esPrimo(int n){
        for(int div = 2; div < n; div++){
            if(n%div == 0)
                return false;
        }
        return true;
    }
    
    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed

        int pos = Hash.buscarPlegamiento(vec, this.jTextBuscar.getText(), vec.length);
        if(pos != -1)
            JOptionPane.showMessageDialog(null, "Clave encontrada en la posición " + (pos+1));
        else
            JOptionPane.showMessageDialog(null, "Clave no encontrada");
        this.jTextBuscar.setText("");
        this.jTextBuscar.requestFocus();

    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jButtonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarActionPerformed
        
        Random random = new Random();
        for(int i = 0; i < vec.length; i++){
            String clave = Integer.toString(random.nextInt(9000)+1000);
            int pos = Hash.plegamiento(vec, clave, vec.length);
            vec[pos] = Integer.parseInt(clave);
            archivo.guardarVecEnArchivo("hash.bin", vec);
            JOptionPane.showMessageDialog(null, "Clave generada: " + clave + ". Se guardo en la posición " + (pos+1));
            this.jTableVector.setValueAt("vec["+(pos+1)+"]", pos, 0);
            this.jTableVector.setValueAt(clave, pos, 1);
        }
        
    }//GEN-LAST:event_jButtonGenerarActionPerformed

