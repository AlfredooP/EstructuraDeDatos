    private void jTextTamañoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextTamañoActionPerformed
        int tam = Integer.parseInt(this.jTextTamaño.getText());
        if(esPrimo(tam)){
            modeloTabla.setRowCount(0);
            modeloTabla.setRowCount(tam);
            vec = new int [tam];
        }
        else{
            while(!esPrimo(tam))
                tam++;
            JOptionPane.showMessageDialog(null, "El tamaño insertado no es primo, por lo que se ha cambiado a "+ tam);
            modeloTabla.setRowCount(0);
            modeloTabla.setRowCount(tam);
            vec = new int [tam];
        }
        
        this.jTextTamaño.setText("");
        this.jTextTamaño.requestFocus();
    }//GEN-LAST:event_jTextTamañoActionPerformed

