    private void jRadioAleatoriosBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioAleatoriosBActionPerformed
        //ALEATORIOS
        matB = new int [(int)this.jSpinnerRB.getValue()][(int)this.jSpinnerCB.getValue()];
        mMB.setRowCount(matB.length);
        mMB.setColumnCount(matB[0].length);
        
        for(int i = 0; i < matB.length; i++){
            for(int j = 0; j < matB[0].length; j++){
                matB[i][j] = random.nextInt(90)+10;
                mMB.setValueAt(Integer.toString(matB[i][j]), i, j);
            }
        }
    }//GEN-LAST:event_jRadioAleatoriosBActionPerformed

