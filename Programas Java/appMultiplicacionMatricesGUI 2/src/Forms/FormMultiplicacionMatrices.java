    public void multiplicar(){
        if(matA[0].length == matB.length){
            matC = new int [matA.length][matB[0].length];
            for(int i = 0; i < matA.length; i++){
                for(int j = 0; j < matB[0].length; j++){
                    matC[i][j] = 0;
                    for(int k = 0; k < matB[0].length; k++)
                        matC[i][j] += matA[i][k] * matB[k][j];
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "No es posible multiplicar las matrices!");
        }
    }
    
    private void jRadioXDefaultAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioXDefaultAActionPerformed
        //X DEFECTO
        matA = new int [(int)this.jSpinnerRA.getValue()][(int)this.jSpinnerCA.getValue()];
        mMA.setRowCount(matA.length);
        mMA.setColumnCount(matA[0].length);
        
        int contador = 1;
        for(int i = 0; i < matA.length; i++){
            for(int j = 0; j < matA[0].length; j++){
                matA[i][j] = contador;
                mMA.setValueAt(Integer.toString(matA[i][j]), i, j);
                contador++;
            }
        }
    }//GEN-LAST:event_jRadioXDefaultAActionPerformed

    private void jRadioCapturarlosAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioCapturarlosAActionPerformed
        //CAPTURARLOS
        matA = new int [(int)this.jSpinnerRA.getValue()][(int)this.jSpinnerCA.getValue()];
        mMA.setRowCount(matA.length);
        mMA.setColumnCount(matA[0].length);
        
        for(int i = 0; i < matA.length; i++){
            for(int j = 0; j < matA[0].length; j++){
                matA[i][j] = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el valor de " + i + "," + j + ":"));
                mMA.setValueAt(Integer.toString(matA[i][j]), i, j);
            }
        }
    }//GEN-LAST:event_jRadioCapturarlosAActionPerformed

