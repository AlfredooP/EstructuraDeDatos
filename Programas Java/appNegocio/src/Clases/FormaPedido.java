    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        if(jComboProducto.getSelectedIndex() == 0 || jSpinnerCantidad.getValue().equals(0)){
            JOptionPane.showMessageDialog(null, "ENTRADA NO VALIDA\nLlene todos los campos para continuar");
        }
        else{
            Producto producto  = new Producto(random.nextInt(9000)+1000, (int)this.jSpinnerCantidad.getValue(), (String)this.jComboProducto.getSelectedItem(),
            descripciones[this.jComboProducto.getSelectedIndex()-1], precios[this.jComboProducto.getSelectedIndex()-1]);
            guardarEnArreglo(producto);
            Ordenamiento.ordenarCodigoAsc(productos, 0, productos.length-1);
            this.jRadioCodigo.setSelected(true);
            archivo.guardarVecEnArchivo("pedidos.bin", productos);
            archivo.leerArchivoEnTabla("pedidos.bin", jTablePedido);
            calcularPrecio();
        }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

    private void jButtonQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonQuitarActionPerformed
        try{
            int codigo = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el código del producto a quitar:"));
            int pos = Busquedas.binariaCodigo(productos, codigo);
            if(pos != -1){
                borrarDeArreglo(pos);
                archivo.guardarVecEnArchivo("pedidos.bin", productos);
                archivo.leerArchivoEnTabla("pedidos.bin", jTablePedido);
                calcularPrecio();
            }
            else
                JOptionPane.showMessageDialog(null, "Codigo del producto no encontrado");
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Código no válido");
        }
    }//GEN-LAST:event_jButtonQuitarActionPerformed

    private void jButtonBusCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBusCodigoActionPerformed
        try{
            int codigo = Integer.parseInt(JOptionPane.showInputDialog(null, "INGRESE EL CÓDIGO DEL PRODUCTO A BUSCAR:\n(Ingrese 0 para buscar todos los productos)"));
            int pos = Busquedas.binariaCodigo(productos, codigo);
            if(pos != -1){
                modeloTabla.setRowCount(1);
                this.jTablePedido.setValueAt(productos[pos].codigo, 0, 0);
                this.jTablePedido.setValueAt(productos[pos].cantidad, 0, 1);
                this.jTablePedido.setValueAt(productos[pos].nombre, 0, 2);
                this.jTablePedido.setValueAt(productos[pos].descripcion, 0, 3);
                this.jTablePedido.setValueAt(productos[pos].precio, 0, 4);
            }
            else if(codigo == 0){
                archivo.guardarVecEnArchivo("pedidos.bin", productos);
                archivo.leerArchivoEnTabla("pedidos.bin", jTablePedido);
            }
            else
                JOptionPane.showMessageDialog(null, "Codigo del producto no encontrado");
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Código no válido");
        }
    }//GEN-LAST:event_jButtonBusCodigoActionPerformed

    private void jButtonBusProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBusProductoActionPerformed
        String nombre = JOptionPane.showInputDialog(null, "INGRESE EL NOMBRE DEL PRODUCTO A BUSCAR:\n(Ingrese 0 para buscar todos los productos)");
        if(nombre.equals("0")){
            archivo.guardarVecEnArchivo("pedidos.bin", productos);
            archivo.leerArchivoEnTabla("pedidos.bin", jTablePedido);
        }
        else{
            int pos = Busquedas.binariaNombre(productos, nombre.toUpperCase());
            if(pos != -1){
                modeloTabla.setRowCount(1);
                this.jTablePedido.setValueAt(productos[pos].codigo, 0, 0);
                this.jTablePedido.setValueAt(productos[pos].cantidad, 0, 1);
                this.jTablePedido.setValueAt(productos[pos].nombre, 0, 2);
                this.jTablePedido.setValueAt(productos[pos].descripcion, 0, 3);
                this.jTablePedido.setValueAt(productos[pos].precio, 0, 4);
            }
            else
                JOptionPane.showMessageDialog(null, "Nombre del producto no encontrado");
        }
    }//GEN-LAST:event_jButtonBusProductoActionPerformed

