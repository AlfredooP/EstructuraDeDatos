    public void calcularPrecio(){
        double totalPagar = 0;
        for(int i = 0; i < modeloTabla.getRowCount(); i++){
                totalPagar += ((double)modeloTabla.getValueAt(i, 4) * (int)modeloTabla.getValueAt(i, 1));
            }
        this.jLabelTotal.setText("$"+totalPagar+"MXN");
    }
    
    public void guardarEnArreglo(Producto p){
        productos = new Producto[aux.length+1];
        for(int i = 0; i < aux.length; i++)
            productos[i] = aux[i];
        productos[productos.length-1] = p;
        aux = productos;
    }
    
    public void borrarDeArreglo(int pos){
        productos = new Producto[aux.length-1];
        for(int i = 0; i < pos; i++)
            productos[i] = aux[i];
        for(int i = aux.length-2; i >= pos; i--)
            productos[i] = aux[i+1];
        aux = productos;
            
    }
    
