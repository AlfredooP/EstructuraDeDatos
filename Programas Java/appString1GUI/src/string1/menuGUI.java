    private void jButtonNumMayusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNumMayusActionPerformed
        //BOTON # DE MAYUSCULAS
        leerYGuardar();
        int contMayus = 0;
        for(int i=0; i < arregloChar.length; i++){
            if(Caracter.esMayuscula(arregloChar[i]))
                contMayus ++;
        }
        this.jLabelResultado.setText("LA CADENA TIENE "+contMayus+" MAYUSCULAS");
    }//GEN-LAST:event_jButtonNumMayusActionPerformed

