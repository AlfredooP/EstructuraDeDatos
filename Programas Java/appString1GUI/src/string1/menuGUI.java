    private void jButtonAMayusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAMayusActionPerformed
        //BOTON A MAYUSCULAS
        leerYGuardar();
        for(int i=0; i < arregloChar.length; i++){
            arregloChar[i] = Caracter.aMayuscula(arregloChar[i]);
        }
        this.jLabelResultado.setText(String.copyValueOf(arregloChar));
    }//GEN-LAST:event_jButtonAMayusActionPerformed

