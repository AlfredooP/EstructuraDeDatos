    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        if(this.jRadioSimple.isSelected()){
            if(!cola.estaVacia()){
                cola.extraer(num);
                if(cola.estaVacia()){
                    for(int i = 0; i < tam; i++)
                        modelo.setValueAt("", 0, i);
                    p = u = 0;
                }
                else
                    modelo.setValueAt("-", 0, u++);
            }
            else{
                JOptionPane.showMessageDialog(null, "La cola está vacía (Underflow)");
            }
        }
        else if(this.jRadioCircular.isSelected()){
            if(!colaCirc.estaVacia()){
                colaCirc.eliminar(num);
                if(colaCirc.estaVacia()){
                    for(int i = 0; i < tam; i++)
                        modelo.setValueAt("", 0, i);
                    p = u = 0;
                }
                else{
                    modelo.setValueAt("", 0, p++);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "La cola está vacía (Underflow)");
            }
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jButtonVaciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVaciarActionPerformed
        if(this.jRadioSimple.isSelected()){
            while(!cola.estaVacia())
                cola.extraer(num);
            for(int i = 0; i < tam; i++)
                        modelo.setValueAt("", 0, i);
            p = u = 0;
        }
        else if(this.jRadioCircular.isSelected()){
            while(!colaCirc.estaVacia())
                colaCirc.eliminar(num);
            for(int i = 0; i < tam; i++)
                        modelo.setValueAt("", 0, i);
            p = u = 0;
        }
    }//GEN-LAST:event_jButtonVaciarActionPerformed

    private void jButtonLlenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLlenarActionPerformed
        if(this.jRadioSimple.isSelected()){
            while(!cola.estaLlena()){
                int rnd = random.nextInt(900)+100;
                cola.agregar(rnd);
                modelo.setValueAt(rnd, 0, p++);
            }
            p = u = 0;
        }
        else if(this.jRadioCircular.isSelected()){
            while(!colaCirc.estaLlena()){
                int rnd = random.nextInt(900)+100;
                colaCirc.insertar(rnd);
                modelo.setValueAt(rnd, 0, p++);
            }
            p = u = 0;
        }
        
    }//GEN-LAST:event_jButtonLlenarActionPerformed

