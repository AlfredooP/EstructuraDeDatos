    private void jSpinnerTamañoStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerTamañoStateChanged
        tam = (int)this.jSpinnerTamaño.getValue();
        if(this.jRadioSimple.isSelected())
            cola = new Cola(tam);
        else if(this.jRadioCircular.isSelected())
            colaCirc = new ColaCircular(tam);
        else{
            JOptionPane.showMessageDialog(null, "Selecciona un tipo de cola para continuar");
            tam = 0;
        }
        modelo.setColumnCount(0);
        modelo.setColumnCount(tam);
        p = u = 0;
    }//GEN-LAST:event_jSpinnerTamañoStateChanged

    private void jButtonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertarActionPerformed
        if(this.jRadioSimple.isSelected()){
            if(!cola.estaLlena()){
                cola.agregar(Integer.parseInt(this.jTextDato.getText()));
                modelo.setValueAt(this.jTextDato.getText(), 0, p++);
                this.jTextDato.setText("");
                if(cola.estaLlena())
                    p = u = 0;
            }
            else{
                JOptionPane.showMessageDialog(null, "La cola está llena (Overflow)");
            }
        }
        else if(this.jRadioCircular.isSelected()){
            if(!colaCirc.estaLlena()){
                colaCirc.insertar(Integer.parseInt(this.jTextDato.getText()));
                modelo.setValueAt(this.jTextDato.getText(), 0, p++);
                this.jTextDato.setText("");
                if(colaCirc.estaLlena())
                    p = u = 0;
            }
            else{
                JOptionPane.showMessageDialog(null, "La cola está llena (Overflow)");
            }
        }      
    }//GEN-LAST:event_jButtonInsertarActionPerformed
