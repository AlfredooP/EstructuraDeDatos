    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        //INTRODUCIR EL DATO
        int val = 0;
        boolean band = true;
        try{
            val = Integer.parseInt(jTextField1.getText());
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "INSERTA UN NUMERO ENTERO", "ERROR, ENTRADA NO VALIDA", JOptionPane.WARNING_MESSAGE);
            jTextField1.requestFocus();
            band = false;
        }
        
        if(band = true)
            modelo.insertRow(modelo.getRowCount(), new Object[]{val});
        this.jTextField1.setText("");
        
        jTextField1.requestFocus();
        
    }//GEN-LAST:event_jTextField1ActionPerformed

