    public void invertirValores(){
        ArrayList valores = new ArrayList();
        for(int ren = 0; ren < modelo.getRowCount(); ren++){
            valores.add(modelo.getValueAt(ren, 0));
        }
        int i=1;
        for(int ren = 0; ren < modelo.getRowCount(); ren++){
            modelo.setValueAt(valores.get(valores.size()-i), ren, 0);
            i++;
        }
        
    }
    
